public class TiposDeDato {
    public void TiposDato (){
        int intVariable = 10;
        double doubleVariable = 10.1234;
        long longVariable = 771709868;
        boolean booleanVariable= true;
        String StringVariable = "Esta es la variable String";

        System.out.println("La variable int guarda numeros enteros: " + intVariable);
        System.out.println("La variable double guarda numeros reales: " + doubleVariable);
        System.out.println("La variable long guarda numeros grandes: " + longVariable);
        System.out.println("La variable boolean guarda datos booleanos: " + booleanVariable);
        System.out.println("La variable String guarda datos alfanumericos: " + StringVariable);
    }
}
